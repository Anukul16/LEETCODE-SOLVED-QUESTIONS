class Solution {
    int isPalindrome(String S) {
        // code here
        int n=S.length();
        int f=0,l=n-1;
        while(f<l){
            if(S.charAt(f)!=S.charAt(l))return 0;
            
            f++;l--;
        }
        return 1;
    }
}